package com.ftn.sbnz.model;

public class BackgroundParams {

    public WayOfLife wayOfLife;
    public Interest interest;
    public PathInLife path;
    public Integer worldly;

    public WayOfLife getWayOfLife() {
        return wayOfLife;
    }

    public void setWayOfLife(WayOfLife wayOfLife) {
        this.wayOfLife = wayOfLife;
    }

    public Interest getInterest() {
        return interest;
    }

    public void setInterest(Interest interest) {
        this.interest = interest;
    }

    public PathInLife getPath() {
        return path;
    }

    public void setPath(PathInLife path) {
        this.path = path;
    }

    public Integer getWorldly() {
        return worldly;
    }

    public void setWorldly(Integer worldly) {
        this.worldly = worldly;
    }
}
